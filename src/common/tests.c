static void testUncompressCPS(void) {
  size_t cpsDataSize = sizeof(cpsData) - 10; // header
  uint8_t *compressedData = malloc(68000);
  size_t compressedDataSize =
      LCWDecompress(cpsData + 10, cpsDataSize, compressedData, 68000);
  assert(compressedDataSize <= 68000);

  uint8_t *output = malloc(cpsDataSize);
  size_t recompSize = LCWCompress(compressedData, output, cpsDataSize);
  printf("%zu\n", recompSize);
  for (int i = 0; i < recompSize; i++) {
    if (i % 8 == 0) {
      printf("\n");
    }
    printf("0X%X ", output[i]);
  }
  printf("\n");
  assert(memcmp(cpsData, output, cpsDataSize) == 0);
  free(output);
  free(compressedData);
}

static void testCompressUncompress(const uint8_t *buffer, size_t bufferSize) {
  uint8_t *compressedBuffer = malloc(64);
  size_t compressedSize = LCWCompress(buffer, compressedBuffer, bufferSize);
  assert(compressedSize <= 64);
  assert(compressedBuffer[compressedSize - 1] == 0X80);

  uint8_t *outBuffer = malloc(bufferSize);
  assert(LCWDecompress(compressedBuffer, compressedSize, outBuffer,
                       bufferSize) == bufferSize);

  assert(memcmp(buffer, outBuffer, bufferSize) == 0);
  free(compressedBuffer);
  free(outBuffer);
}

